CREATE OR REPLACE PACKAGE BODY MY_PACK

IS

	FUNCTION ANNSAL (VEMPNO EMP.EMPNO%TYPE)
		RETURN NUMBER
	IS
		VANNSAL NUMBER(10) := 0;

	BEGIN
		SELECT SAL * 12 + NVL(COMM, 0)
			INTO VANNSAL
		FROM EMP
		WHERE EMPNO = VEMPNO;

		RETURN VANNSAL;

	END ANNSAL;


	PROCEDURE RAISE_SAL (VEMPNO EMP.EMPNO%TYPE, VRATIO NUMBER)

	IS

	BEGIN
		UPDATE EMP
		SET SAL = SAL*(1+VRATIO/100)
		WHERE EMPNO = VEMPNO;

	END RAISE_SAL;
	




END;
