CREATE FUNCTION FIND_SAL(VEMPNO EMP.EMPNO%TYPE)
	RETURN NUMBER
IS
	VSAL EMP.SAL%TYPE := 0;
BEGIN
	SELECT SAL
		INTO VSAL
	FROM EMP
	WHERE EMPNO = VEMPNO;

	RETURN VSAL;

END;
